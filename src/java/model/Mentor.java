package model;

/**
 *
 * @author ADMIN
 */
public class Mentor {

    private int id;
    private String avatar;
    private User user;

    public Mentor() {
    }

    public Mentor(int id, String avatar, User user) {
        this.id = id;
        this.avatar = avatar;
        this.user = user;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public String getAvatar() {
        return avatar;
    }

    public void setAvatar(String avatar) {
        this.avatar = avatar;
    }

    @Override
    public String toString() {
        return "Mentor{" + "id=" + id + ", avatar=" + avatar + ", user=" + user + '}';
    }

}
