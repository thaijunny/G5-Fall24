package model;


public class Mentee {
    private int id;
    private User user;

    public Mentee() {
    }

    public Mentee(int id, User user) {
        this.id = id;
        this.user = user;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    @Override
    public String toString() {
        return "Mentee{" + "id=" + id + ", user=" + user + '}';
    }
}
